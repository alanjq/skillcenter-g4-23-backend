class Persona
{
    private string nombre = "";
    private string apellido = "";
    int edad = 15:

    public setNombre(string newNombre)
    {
        this.nombre = newNombre;
    }

    public getNombre()
    {
        return this.nombre;
    }
   
    public setApellido(string newApellido)
    {
        this.apellido = newApellido;
    }

    public getApellido()
    {
        return this.apellido;
    }

    public setEdad(int newEdad)
    {
        this.edad = newEdad;
    }

    public getEdad()
    {
        return this.edad;
    }
}
